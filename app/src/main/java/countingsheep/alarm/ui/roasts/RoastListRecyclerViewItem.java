package countingsheep.alarm.ui.roasts;

public class RoastListRecyclerViewItem {

    private String content;
    private int playimageResourceId;
    private int pauseResourceId;

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public int getPlayimageResourceId() {
        return playimageResourceId;
    }

    public void setPlayimageResourceId(int playimageResourceId) {
        this.playimageResourceId = playimageResourceId;
    }

    public int getPauseResourceId() {
        return pauseResourceId;
    }

    public void setPauseResourceId(int pauseResourceId) {
        this.pauseResourceId = pauseResourceId;
    }
}
